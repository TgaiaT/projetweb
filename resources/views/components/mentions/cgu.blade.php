            </p>
            <p>
                L’utilisateur peut, s’il le souhaite, s’opposer à la mise en place de cookies. Pour cela, ce dernier peut :
            </p>
            <ul>
                <li>
                    •	Refuser l’utilisation des cookies lors de sa première connexion sur le site en cliquant sur le bandeau prévu à cet effet,<br>
